/**
 * 获取图标HTML
 * @param {string} iconName - 图标名称
 * @param {object} options - 可选参数 {className: string, style: string}
 * @returns {string} SVG HTML字符串
 */
function getIcon(iconName, options = {}) {
    const icon = IconLibrary[iconName];
    if (!icon) {
        console.warn(`Icon "${iconName}" not found in IconLibrary`);
        return '';
    }

    let className = options.className || '';
    let style = options.style || '';

    // 注入class和style
    let result = icon;
    if (className) {
        result = result.replace('<svg', `<svg class="${className}"`);
    }
    if (style) {
        result = result.replace('<svg', `<svg style="${style}"`);
    }

    return result;
}

/**
 * 在指定元素中渲染图标
 * @param {string} iconName - 图标名称
 * @param {HTMLElement|string} element - 目标元素或选择器
 * @param {object} options - 可选参数
 */
function renderIcon(iconName, element, options = {}) {
    const targetElement = typeof element === 'string'
        ? document.querySelector(element)
        : element;

    if (!targetElement) {
        console.warn(`Element not found: ${element}`);
        return;
    }

    targetElement.innerHTML = getIcon(iconName, options);
}

